export type Language = "pt" | "ja" | "en";

export const languageStorageKey = "okh-workledger-language";

export const languages: Array<{ code: Language; label: string }> = [
  { code: "pt", label: "Português" },
  { code: "ja", label: "日本語" },
  { code: "en", label: "English" }
];

export const defaultLanguage: Language = "pt";

export const monthNamesByLanguage: Record<Language, string[]> = {
  pt: ["Janeiro", "Fevereiro", "Março", "Abril", "Maio", "Junho", "Julho", "Agosto", "Setembro", "Outubro", "Novembro", "Dezembro"],
  ja: ["1月", "2月", "3月", "4月", "5月", "6月", "7月", "8月", "9月", "10月", "11月", "12月"],
  en: ["January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"]
};

export function isLanguage(value: unknown): value is Language {
  return typeof value === "string" && languages.some((language) => language.code === value);
}

export function languageToHtmlLang(language: Language) {
  if (language === "ja") return "ja";
  if (language === "en") return "en";
  return "pt-BR";
}

export function getMonthName(language: Language, month: number) {
  return monthNamesByLanguage[language][month - 1] ?? monthNamesByLanguage[defaultLanguage][month - 1] ?? "";
}

export function formatMonthYear(language: Language, month: number, year: number) {
  if (language === "ja") return `${year}年${String(month).padStart(2, "0")}月`;
  if (language === "en") return `${getMonthName(language, month)} ${year}`;
  return `${getMonthName(language, month)} de ${year}`;
}

